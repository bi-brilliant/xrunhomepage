        $.ajax({
            url: './qna_action.php',
            type: 'POST',
            data: {
                'type': 'list'
            },
            success: function(data) {
                data = $.parseJSON(data)
                
                var html = ''

                $.each(data, function() {

                    html += '<tr>'
                        html += '<td>' + this.email + '</td>'
                        html += '<td><a class="show-detail" data-idx="' + this.idx + '" href="">' + this.subject + '</a></td>'
                        html += '<td>' + this.regdate.split(' ')[0] + '</td>'
                    html += '</tr>'
                })

                $('.table-questions tbody').html(html)

                $('.show-detail').click(function(e) {
                    e.preventDefault()

                    var idx = $(this).attr('data-idx')

                    $.ajax({
                        url: './qna_action.php',
                        type: 'POST',
                        data: {
                            'type': 'detail',
                            'idx': idx
                        },
                        success: function(detail) {
                            detail = $.parseJSON(detail)
                            
                            $('.detail-title').html(detail.subject)
                            $('.detail-from-time').html('From: ' + detail.email + '<span class="mailbox-read-time pull-right">' + detail.regdate + '</span>')
                            $('.detail-content').html('<p>' + detail.content + '</p>')
                        }
                    })
                })
            }
        })

